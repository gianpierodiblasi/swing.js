package giada.awt;

/**
 * The java.awt.Insets clone
 *
 * @author gianpiero.diblasi
 */
public class Insets {

  public int top;
  public int left;
  public int bottom;
  public int right;

  public Insets(int top, int left, int bottom, int right) {
    this.top = top;
    this.left = left;
    this.bottom = bottom;
    this.right = right;
  }
}
