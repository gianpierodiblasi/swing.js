package javascript.awt;

/**
 * The java.awt.Point clone
 *
 * @author gianpiero.diblasi
 */
public class Point {

  public final int x;
  public final int y;

  /**
   * Creates the object
   *
   * @param x The x-axis coordinate
   * @param y The y-axis coordinate
   */
  public Point(int x, int y) {
    this.x = x;
    this.y = y;
  }
}
